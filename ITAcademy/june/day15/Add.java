package ITAcademy.june.day15;

public class Add {
	
	int add(int x, int y) { //정수형 두개 
		return x + y;
		
	}
	
	float add(float x, float y) {
		return x + y;
	}
	
	
	String add(String x, String y) {
		return x + y;
	}
	
	int add(int x, int y, int z) {
		return x + y + z;  //정수형 세개
	}
}
